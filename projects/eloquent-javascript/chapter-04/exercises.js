////////////////////////////////////////////////////////////////////////////////

function range(start, end, step) {
  if(step === undefined){
    step = (end - start)/(Math.abs(end - start));
  }

  var results = [];
  for(var i = start; i !== end; i += step){
    results.push(i);
  }

  results.push(end);

  return results;
}

