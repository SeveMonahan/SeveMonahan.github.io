////////////////////////////////////////////////////////////////////////////////
// range ///////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////

function range(start, end, step) {
  console.log(`start ${start} end ${end} step ${step}`);

  if(step === undefined){
    step = 1;
  }

  var results = [];

  if(start === end){
    return [];
  }

  if(start < end && 0 < step){
    for(var i = start; i <= end; i+=step){
      results.push(i);
    }
    
    return results;
  }
  return [];
}

