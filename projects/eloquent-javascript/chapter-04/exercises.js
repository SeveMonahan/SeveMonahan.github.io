function sum(array) {
  return array.reduce((acc, curr) => acc + curr, 0);
}

////////////////////////////////////////////////////////////////////////////////
// reverseArray ////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////

function reverseArray(array) {
  var results = [];
  for (var i = array.length - 1; i--; i >= 0){
    results.push(array[i]);
  }

  return results;
}

////////////////////////////////////////////////////////////////////////////////
// reverseArrayInPlace /////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////

function reverseArrayInPlace(array) {
  array = reverseArray(array);
}

