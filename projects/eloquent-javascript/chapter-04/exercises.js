////////////////////////////////////////////////////////////////////////////////
// range ///////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////

function range(start, end, step) {
  var results = [];

  if(start === end){
    return [];
  }
  
  if(end > start){
    for(var i = start; i += step; i < end){
        results.push(i);
    }
    results.push(end);
    return results;
  }
  return [];
}

